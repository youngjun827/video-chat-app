import axios from "axios";
import { logout } from "./shared/utils/auth";

// 6. The API exists in the "apiClient". This means all activities between the frontend and backend environment go through here. So, naturally, THROUGH the apiClient, the userDetails are passed to the server. **Please scroll the page down to see step 7.
const apiClient = axios.create({
  baseURL: "https://youngjun-video-chat-app.herokuapp.com/api",
  // timeout: 1000,
});

apiClient.interceptors.request.use(
  (config) => {
    const userDetails = localStorage.getItem("user");

    if (userDetails) {
      const token = JSON.parse(userDetails).token;
      config.headers.Authorization = `Bearer ${token}`;
    }

    return config;
  },
  (err) => {
    return Promise.reject(err);
  }
);

// public routes

// 7. Here, the "login" function tries to POST our userDetails (data) to the "/auth/login" route. To check the route, we must go to our backend environment. **Please go to the "authRoutes" file in the backend folder to view the routing process.
export const login = async (data) => {
  try {
    return await apiClient.post("/auth/login", data);
  } catch (exception) {
    return {
      error: true,
      exception,
    };
  }
};

